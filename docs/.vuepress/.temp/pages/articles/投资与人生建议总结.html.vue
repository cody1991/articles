<h2 id="一、投资建议" tabindex="-1"><a class="header-anchor" href="#一、投资建议"><span>一、投资建议</span></a></h2>
<h3 id="_1-资产配置原则" tabindex="-1"><a class="header-anchor" href="#_1-资产配置原则"><span>1. 资产配置原则</span></a></h3>
<p><strong>多币种、全球化配置</strong></p>
<ul>
<li>不要持有单一币种资产，净资产500万以上应开始配置多币种资产，尤其是美元资产</li>
<li>资产配置从单币种转向多币种，从区域化转向全球化</li>
<li>从世界的角度看中国，而不是从中国的角度看世界</li>
</ul>
<p><strong>资产结构建议</strong></p>
<ul>
<li>美股：37%（科技股为主）</li>
<li>现金：32%（保持充足现金流）</li>
<li>不动产：20%（逐步降低到15-17%）</li>
<li>美债和保险：11%（逐步增加）</li>
</ul>
<p><strong>三层账户体系</strong></p>
<ul>
<li><strong>进取型账户</strong>：追求高收益，承担高风险</li>
<li><strong>稳健型账户</strong>：攻守平衡，做中轴</li>
<li><strong>防守型账户</strong>：压舱石角色，追求稳定（40%+配置）</li>
</ul>
<h3 id="_2-仓位管理" tabindex="-1"><a class="header-anchor" href="#_2-仓位管理"><span>2. 仓位管理</span></a></h3>
<p><strong>永不满仓原则</strong></p>
<ul>
<li>一般保持7-7.5成仓位，最高不超过8.5成</li>
<li>不满仓就是给自己留操作空间，等于给自己手上留备用的牌</li>
<li>确保任何时候都有下注的筹码，关键时刻不会掉链子</li>
</ul>
<p><strong>金字塔加仓法</strong></p>
<ul>
<li>越跌越买，分批买入，摊低成本</li>
<li>价格档差要拉开：百元以上个股，档差5-10美元</li>
<li>例如英伟达：126→120→110→100，档差逐步拉开</li>
</ul>
<p><strong>倒金字塔卖出法</strong></p>
<ul>
<li>越涨越卖，分批卖出，提升收益率</li>
<li>达到预期目标后及时止盈，不要贪多</li>
</ul>
<h3 id="_3-选股策略" tabindex="-1"><a class="header-anchor" href="#_3-选股策略"><span>3. 选股策略</span></a></h3>
<p><strong>&quot;第一&quot;或&quot;唯一&quot;原则</strong></p>
<ul>
<li>第一：规模和市场地位</li>
<li>唯一：核心竞争力</li>
<li>最好兼具&quot;第一和唯一&quot;</li>
</ul>
<p><strong>基本面分析</strong></p>
<ul>
<li><strong>三表分析</strong>：
<ul>
<li>利润表：看是否赚钱，是否有成长性</li>
<li>资产负债表：看是否健康，现金是否覆盖债务</li>
<li>现金流量表：判断赚钱能力是否真实</li>
</ul>
</li>
<li><strong>行业前景</strong>：
<ul>
<li>市场空间（天花板）</li>
<li>政策利好和风向</li>
<li>竞争优势（第一或唯一）</li>
</ul>
</li>
<li><strong>PE分析</strong>：作为参考，结合历史PE和同行业对比</li>
</ul>
<p><strong>看不懂的不投</strong></p>
<ul>
<li>看不懂就有可能在被套时不知道该怎么做</li>
<li>哪怕能赚钱，看不懂的坚决不投</li>
</ul>
<h3 id="_4-投资标的" tabindex="-1"><a class="header-anchor" href="#_4-投资标的"><span>4. 投资标的</span></a></h3>
<p><strong>美股重点板块</strong></p>
<ul>
<li><strong>科技股</strong>：美股七巨头（英伟达、苹果、微软、谷歌、Meta、亚马逊、特斯拉）+ 台积电</li>
<li><strong>消费股</strong>：Costco、沃尔玛、宝洁、麦当劳</li>
<li><strong>生物医药</strong>：礼来、联合健康、诺和诺德、默沙东、强生</li>
<li><strong>宽基指数</strong>：纳指（QQQ）、标普500（SPY）</li>
</ul>
<p><strong>其他资产</strong></p>
<ul>
<li><strong>美债</strong>：全球金融系统底层资产，长期配置理想选择</li>
<li><strong>港险</strong>：类信托产品，资产阻尼器，进可攻退可守</li>
<li><strong>不动产</strong>：海外农业用地、商业项目，租金收益3-4%</li>
</ul>
<h3 id="_5-投资心态与策略" tabindex="-1"><a class="header-anchor" href="#_5-投资心态与策略"><span>5. 投资心态与策略</span></a></h3>
<p><strong>长期主义</strong></p>
<ul>
<li>不要被短期波动裹挟，放眼长期</li>
<li>通过时间放大复利效应</li>
<li>长期持有是价值投资的最好方式</li>
<li>耐心是稀缺品，真正让你优于常人的是耐心、慎重地对待重大机会</li>
</ul>
<p><strong>风险控制优先</strong></p>
<ul>
<li>投资的首要前提是保值，其次才是增值</li>
<li>先谈防守，再说进攻</li>
<li>先保值/保住本金，再增值/追求收益</li>
<li>控制好安全边际：仓位、资金、成本</li>
</ul>
<p><strong>现金流管理</strong></p>
<ul>
<li>保持充足现金流，应对突发状况</li>
<li>有能力在出现突发状况时应对自如</li>
<li>市场下跌时买入更多股票等回升时获得更高收益</li>
</ul>
<p><strong>建立自己的投资体系</strong></p>
<ul>
<li>体系是盈利的根基</li>
<li>帮助克服人性弱点，避免成为墙头草</li>
<li>帮助梳理和建立自己的能力圈</li>
<li>找到属于自己的节奏</li>
</ul>
<h3 id="_6-操作技巧" tabindex="-1"><a class="header-anchor" href="#_6-操作技巧"><span>6. 操作技巧</span></a></h3>
<p><strong>做T策略</strong></p>
<ul>
<li>震荡行情中，逢低买入，逢高卖出</li>
<li>价格档差要拉开，才能有效拉低成本</li>
<li>不要几块钱就设置加仓，没有意义</li>
</ul>
<p><strong>期权护仓</strong></p>
<ul>
<li>下跌行情中，期权做空（小比例、部分覆盖）护仓</li>
<li>跌到点了就买入正股做多，等待上涨</li>
<li>可以赚两笔钱</li>
</ul>
<p><strong>负成本策略</strong></p>
<ul>
<li>通过分批卖出，将持仓成本做成负成本</li>
<li>负成本后长期持有，哪怕跌了仍旧继续持有</li>
<li>这是对抗人性恐惧的有效方式</li>
</ul>
<p><strong>止盈止损</strong></p>
<ul>
<li>止损重要，止盈更重要</li>
<li>达到预期后及时离场，不要贪多</li>
<li>理性＞赌性的人，大多数可以&quot;富一世&quot;</li>
</ul>
<h3 id="_7-市场判断" tabindex="-1"><a class="header-anchor" href="#_7-市场判断"><span>7. 市场判断</span></a></h3>
<p><strong>2025年展望</strong></p>
<ul>
<li>美股将在&quot;震荡中上行&quot;，震荡是主要特征</li>
<li>人工智能利好将持续</li>
<li>关注三个利空：美债问题、关税问题、通胀问题</li>
<li>如果基本面没有恶化，下跌就是买入的好节点</li>
</ul>
<p><strong>投资三要素</strong></p>
<ul>
<li>资金：准备充足的现金，随时用于市场抄底</li>
<li>耐心：等待机会，不要频繁操作</li>
<li>勇气：机会来了，要有资金和勇气，敢于下手</li>
</ul>
