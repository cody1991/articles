<h2 id="二、人生建议" tabindex="-1"><a class="header-anchor" href="#二、人生建议"><span>二、人生建议</span></a></h2>
<h3 id="_1-核心原则" tabindex="-1"><a class="header-anchor" href="#_1-核心原则"><span>1. 核心原则</span></a></h3>
<p><strong>人对了，事就对了</strong></p>
<ul>
<li>事是人做出来的，人在整个事情的发展进程中是决定性因素</li>
<li>投资人，其实就是投资&quot;人&quot;</li>
<li>人不对，商业模式再好也没用</li>
<li>人品靠谱、实战经验丰富的人最受欢迎</li>
</ul>
<p><strong>选择比努力重要</strong></p>
<ul>
<li>选择不对，努力白费</li>
<li>要站在矛盾之上，而不是矛盾之中</li>
<li>要有敏锐的洞察力，能感受市场的变化</li>
</ul>
<p><strong>见识比学识重要</strong></p>
<ul>
<li>见识是经历，是经验，是眼界，是理论和实践的结合，是超前的眼光</li>
<li>学识停留在理论层面，可能是别人的理论，有可能是错的</li>
<li>&quot;读万卷书，不如行万里路；行万里路，不如阅人无数&quot;</li>
</ul>
<h3 id="_2-财富观" tabindex="-1"><a class="header-anchor" href="#_2-财富观"><span>2. 财富观</span></a></h3>
<p><strong>财富的真谛</strong></p>
<ul>
<li>不是一下子能够进来多少钱</li>
<li>而是不断感受小钱由小变大带来的变化</li>
<li>能够准确理解这个社会在发生什么</li>
</ul>
<p><strong>财富自由</strong></p>
<ul>
<li>本质是&quot;对生活的选择权&quot;</li>
<li>普通人难以实现的核心原因：社会规则、认知局限与资源分配共同编织的网</li>
<li>突破的关键不是&quot;更努力&quot;，而是转换思维模式</li>
</ul>
<p><strong>赚钱与守钱</strong></p>
<ul>
<li>赚钱是一场意外，守住钱才是一项能力</li>
<li>创富是运气，守富是能力</li>
<li>大多数人一辈子都在赚钱，但他们只是金钱的中转站，从未真正拥有过</li>
<li>赚到钱后，要从激进的、充满欲望的&quot;进攻&quot;，开始转向低欲望、绝对理性的&quot;防守&quot;</li>
</ul>
<p><strong>掌握生产资料</strong></p>
<ul>
<li>尽一切可能去掌握生产资料</li>
<li>放弃鸡娃，鸡娃不如鸡自己</li>
<li>学历贬值会越来越严重</li>
<li>社会上升通道到他们那个时候，基本上完全闭合</li>
<li>还不如在自己这辈，尽可能给他们安排好，鸡自己的效益会好很多</li>
</ul>
<h3 id="_3-家庭与教育" tabindex="-1"><a class="header-anchor" href="#_3-家庭与教育"><span>3. 家庭与教育</span></a></h3>
<p><strong>家庭关系</strong></p>
<ul>
<li>家庭要往上走，要力出一孔，往一个方向使力</li>
<li>双方要相互理解、相互尊重、相互信任、相互包容、相互扶持、共同进步</li>
<li>主动把想法跟自己的伴侣共享，让她主动成为你的助力</li>
<li>男女要想感情好和长久，一方对另一方或多或少要有点崇拜的感情成分</li>
</ul>
<p><strong>兄弟姐妹关系</strong></p>
<ul>
<li>兄弟姐妹是父母在人世间给你们彼此找来的帮手</li>
<li>以后父母会老去，化作尘土，而你们仍可以相互帮扶，走向未来</li>
<li>让他们用培养帮手/左膀右臂的心态，去适应彼此间的关系</li>
<li>根源在于父母思想上是否一碗水端平，行为上是否根据每个孩子的特性和喜好，有所偏爱</li>
</ul>
<p><strong>教育理念</strong></p>
<ul>
<li>对孩子没有太高的期望值，身心健康、人格健全、三观正就行</li>
<li>把孩子培养成普通且幸福的人</li>
<li>只有成为普通且幸福的人，他才有机会争取成为优秀/卓越且幸福的人</li>
<li>幼年乃至童年教育：教他们爱，教他们人生的美好，教他们希望，教他们世上各种知识的有趣</li>
<li>当他们拥有了对生命发自内心的热爱之后，再残酷的社会，也打不垮他们</li>
<li>培养孩子的质疑精神，引导他们去思考，助力孩子们培养看本质和底层逻辑的眼光</li>
</ul>
<h3 id="_4-个人成长" tabindex="-1"><a class="header-anchor" href="#_4-个人成长"><span>4. 个人成长</span></a></h3>
<p><strong>与自己和解</strong></p>
<ul>
<li>学会面对自己的普通很重要</li>
<li>在绝对的天赋面前，很多人所谓的&quot;优秀&quot;不堪一击</li>
<li>最重要的是，还是和自己和解，不断跨过曾经的自己，就是成长</li>
<li>希望自己的娃们能做到&quot;跟自己比&quot;，而不是过多地关注他人</li>
</ul>
<p><strong>持续学习</strong></p>
<ul>
<li>一个始终处于不断学习，打破原有阶层束缚不断进步和兑变的人，是最可怕、最可敬的</li>
<li>学习逻辑学，有了逻辑，就不容易被表面的东西困住，不容易被忽悠</li>
<li>匹配丰富的经历和阅历，就会逐步具备穿透现象看本质的能力</li>
</ul>
<p><strong>心态修炼</strong></p>
<ul>
<li>早点意识到社会和现实的残酷，人就飘不起来</li>
<li>要么去接触更高维度的群体，汲取他们身上的能量</li>
<li>要么去看看自己买不起的东西</li>
<li>要么去医院走走看看</li>
<li>从年轻时的意气风发，到中年时的&quot;非常谦逊&quot;，说到底，就是你（成）&quot;熟&quot;了</li>
</ul>
<p><strong>屏蔽能力</strong></p>
<ul>
<li>屏蔽能力很重要，可以最大限度地减少噪音对自己的干扰</li>
<li>那些给你负面反馈的人，往往是最没有价值的群体</li>
<li>不要沉迷宏大叙事，更不要沉迷宏大叙事的集体荣誉</li>
<li>这种忽略了个体权益的集体荣誉，一文不值</li>
</ul>
<h3 id="_5-人生哲学" tabindex="-1"><a class="header-anchor" href="#_5-人生哲学"><span>5. 人生哲学</span></a></h3>
<p><strong>找到属于自己的节奏</strong></p>
<ul>
<li>建立适合自己的投资体系，找到属于自己的节奏</li>
<li>每个人的情况都不同，因此操作上也会不一样</li>
<li>适合自己的才是最好的，不要照搬照抄</li>
</ul>
<p><strong>专注与极致</strong></p>
<ul>
<li>要趁早找到自己真正擅长且有价值的事情，把它修炼到极致</li>
<li>真正的财富，并不来自于你会的东西很多，相反是来自于你会的东西越少越好</li>
<li>专注、聚焦、极致</li>
<li>人的精力是有限的，不贪心，专注做自己有把握的事，去深耕</li>
</ul>
<p><strong>长期视角</strong></p>
<ul>
<li>在有限的生命之中，不断地去体悟&quot;道&quot;（客观规律）的规律</li>
<li>试图在已经定死的命数之中寻找一丝丝缝隙</li>
<li>然后以顽强的意志去改变一些原本不能改变的，并且越早越好</li>
<li>你三十岁懂得的东西，有些人到了五十岁可能都未必懂，甚至有些人这一生都活在笼子里</li>
<li>但社会比的，就是谁早懂</li>
</ul>
<p><strong>允许一切发生</strong></p>
<ul>
<li>做投资就像做事，最重要的不是预测对，而是无论发生什么都能自如应对</li>
<li>允许一切发生，除了心态平和外，更重要的是因为胸有成竹，所以能坦然处之</li>
</ul>
<p><strong>向前看</strong></p>
<ul>
<li>踏空或卖飞，这是常态，不必过多纠结</li>
<li>往前看，震荡的市场，会有机会的</li>
<li>放下对于过去、现在的执着，不要着相，往前看，往前走</li>
</ul>
<h3 id="_6-社会认知" tabindex="-1"><a class="header-anchor" href="#_6-社会认知"><span>6. 社会认知</span></a></h3>
<p><strong>阶层跃迁</strong></p>
<ul>
<li>阶层是很难跨越的，有钱也不行，看的还是资源和背景</li>
<li>阶层跃迁需要野蛮生长，需要打破规则的勇气，也需要幸运之神的眷顾</li>
<li>人发达和升维的过程，就是不断抛弃原先的环境和朋友，向新的平衡走的过程</li>
</ul>
<p><strong>规则与限制</strong></p>
<ul>
<li>容易挣钱的地方，都有限制</li>
<li>限制的逻辑很简单，越麻烦、越稀缺，越赚钱</li>
<li>信息不对称、法律灰区、制度红利，这些才是真正的财富源泉</li>
<li>规则才是决定胜负的根本</li>
</ul>
<p><strong>信心与行动</strong></p>
<ul>
<li>不要看它们怎么说，要看它们怎么做</li>
<li>钱去向哪里，就是对哪里有信心，这是真的&quot;信心&quot;</li>
<li>钱逃离哪里，就是对哪里没信心，哪怕它们嘴上说得很有&quot;信心&quot;</li>
</ul>
<p><strong>世界很大</strong></p>
<ul>
<li>世界很大，多走走看看，不要活在信息茧房中</li>
<li>有些东西经历了、看过了，会豁然开朗</li>
<li>往外走，往外看，不跟国人抢国内有限的机会和资源，不内卷</li>
</ul>
